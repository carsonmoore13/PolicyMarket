/**
 * fetchDistrictCentroids.mjs
 *
 * Fetches TX congressional, state senate, and state house district centroids
 * from the Census TIGER REST API and regenerates
 * frontend/src/utils/districtCentroids.js
 *
 * Run: node scripts/fetchDistrictCentroids.mjs
 */

import axios from "axios";
import { writeFileSync } from "fs";
import { resolve, dirname } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const TIGER = "https://tigerweb.geo.census.gov/arcgis/rest/services/TIGERweb/Legislative/MapServer";
const http = axios.create({ timeout: 25000 });

async function paginatedQuery(layerId, fields, where = "STATE='48'") {
  const all = [];
  let offset = 0;
  while (true) {
    const res = await http.get(`${TIGER}/${layerId}/query`, {
      params: { where, outFields: fields.join(","), returnGeometry: false, f: "json", resultRecordCount: 200, resultOffset: offset },
    });
    const features = res.data?.features || [];
    all.push(...features);
    if (!res.data?.exceededTransferLimit || features.length === 0) break;
    offset += features.length;
  }
  return all;
}

async function fetchCongressional() {
  console.log("Fetching TX Congressional Districts (layer 0 — 119th Congress)…");
  const features = await paginatedQuery(0, ["CD119", "INTPTLAT", "INTPTLON"]);
  const result = {};
  for (const f of features) {
    const a = f.attributes || {};
    const num = parseInt(a.CD119, 10);
    const lat = parseFloat(a.INTPTLAT);
    const lng = parseFloat(a.INTPTLON);
    if (num && !isNaN(lat) && !isNaN(lng)) {
      result[`TX-${num}`] = [+lat.toFixed(4), +lng.toFixed(4)];
    }
  }
  console.log(`  → ${Object.keys(result).length} congressional districts`);
  return result;
}

